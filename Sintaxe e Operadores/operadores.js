var b = '2';
console.log(a == b);//TRUE, mesmo não sendo do mesmo tipo
console.log(a === b);//FALSE, tem que ser mesmo tipo e mesmo valor
console.log(a != b);//FALSE, eles são iguais, ambos são 2
console.log(a !== b);//TRUE, tem que ser o mesmo tipo e valor
